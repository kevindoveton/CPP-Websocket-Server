enum WebSocketFrameModes {
  continuation = 0,
  text = 1,
  binary = 2
};

#include <string>
#include <iostream>
#include <bitset>

class WebSocketFrame {
  public: 
    explicit WebSocketFrame();
    WebSocketFrame(std::string);

    bool readFrame(std::string f);
  private:
    bool _fin;
    int _mode;
    bool _maskBit;
    unsigned long _payloadLength;
    std::bitset<8> _mask[4]; // 32 bits
    void reset();
};

